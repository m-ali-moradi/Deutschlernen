                        fontSize: 11, color: Color(0xFF2563EB))),
              ),
            ])),

        const SizedBox(height: 14),

        Row(children: [
          Expanded(
              child: GestureDetector(
            onTap: () => setState(() {
              _flashcardMode = true;
              _selectedWord = null;
              _flashcardIndex = 0;
              _isFlipped = false;
            }),
            child: Container(
              padding: const EdgeInsets.symmetric(vertical: 14),
              decoration: BoxDecoration(
                gradient: const LinearGradient(
                    colors: [Color(0xFF3B82F6), Color(0xFF9333EA)]),
                borderRadius: BorderRadius.circular(14),
              ),
              child: const Center(
                  child: Text('Flashcard starten',
                      style: TextStyle(
                          fontSize: 14,
                          fontWeight: FontWeight.w600,
                          color: Colors.white))),
            ),
          )),
        ]),
      ]),
    );
  }

  // ── FLASHCARD MODE ────────────────────────────────────────────────────────
  Widget _buildFlashcard(bool isDark) {
    final words = _filtered.isNotEmpty ? _filtered : _words;
    if (words.isEmpty) return const Center(child: Text('Keine Wörter'));
    final w = words[_flashcardIndex % words.length];
    final progress = (_flashcardIndex + 1) / words.length;
    final textMuted =
        isDark ? const Color(0xFF94A3B8) : const Color(0xFF6B7280);

    return Padding(
      padding: const EdgeInsets.fromLTRB(20, 24, 20, 16),
      child: Column(children: [
        // Top bar
        Row(mainAxisAlignment: MainAxisAlignment.spaceBetween, children: [
          _BackButton(
              isDark: isDark,
              onTap: () => setState(() {
                    _flashcardMode = false;
                    _flashcardIndex = 0;
                    _isFlipped = false;
                  })),
          Text('${_flashcardIndex + 1} / ${words.length}',
              style: TextStyle(fontSize: 13, color: textMuted)),
        ]),
        const SizedBox(height: 14),

        // Progress
        ClipRRect(
          borderRadius: BorderRadius.circular(100),
          child: LinearProgressIndicator(
            value: progress,
            minHeight: 8,
            backgroundColor:
                isDark ? const Color(0xFF1E293B) : const Color(0xFFE2E8F0),
            valueColor: const AlwaysStoppedAnimation(Color(0xFF3B82F6)),
          ),
        ),
        const SizedBox(height: 32),

        // Flashcard
        Expanded(
          child: GestureDetector(
            onTap: () => setState(() => _isFlipped = !_isFlipped),
            child: AnimatedSwitcher(
              duration: const Duration(milliseconds: 300),
              transitionBuilder: (child, anim) =>
                  RotationYTransition(turns: anim, child: child),
              child: _isFlipped
                  ? _FlashcardBack(
                      key: const ValueKey('back'), word: w, isDari: _isDari)
                  : _FlashcardFront(key: const ValueKey('front'), word: w),
            ),
          ),
        ),

        const SizedBox(height: 20),

        // Difficulty buttons
        Row(children: [
          Expanded(
              child: _DiffBtn(
                  label: 'Schwer',
                  emoji: '❌',
                  bg: isDark
                      ? const Color(0xFF450A0A)
                      : const Color(0xFFFFF1F2),
                  fg: const Color(0xFFEF4444),
                  onTap: () => _nextFlashcard('hard', words.length))),
          const SizedBox(width: 10),
          Expanded(
              child: _DiffBtn(
                  label: 'Mittel',
                  emoji: '🤔',
                  bg: isDark
                      ? const Color(0xFF1C1A09)
                      : const Color(0xFFFEFCE8),
                  fg: const Color(0xFFF59E0B),
                  onTap: () => _nextFlashcard('medium', words.length))),
          const SizedBox(width: 10),
          Expanded(
              child: _DiffBtn(
                  label: 'Leicht',
                  emoji: '✅',
                  bg: isDark
                      ? const Color(0xFF052E16)
                      : const Color(0xFFF0FDF4),
                  fg: const Color(0xFF22C55E),
                  onTap: () => _nextFlashcard('easy', words.length))),
        ]),
        const SizedBox(height: 8),
      ]),
    );
  }

  void _nextFlashcard(String difficulty, int total) {
    final words = _filtered.isNotEmpty ? _filtered : _words;
    final w = words[_flashcardIndex % words.length];
    setState(() {
      w.difficulty = difficulty;
      _isFlipped = false;
      _flashcardIndex = (_flashcardIndex + 1) % total;
    });
  }
}

