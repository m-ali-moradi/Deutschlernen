  String _search = '';
  String? _selectedCategory;
  bool _flashcardMode = false;
  VocabWord? _selectedWord;
  int _flashcardIndex = 0;
  bool _isFlipped = false;
  bool _isDari = false;

  final _tabs = const [
    ('words', 'Wörter'),
    ('phrases', 'Phrasen'),
    ('favorites', 'Favoriten'),
    ('difficult', 'Schwierig'),
  ];

  late final List<VocabWord> _words = _sampleWords
      .map((w) => VocabWord(
            id: w.id,
            german: w.german,
            phonetic: w.phonetic,
            english: w.english,
            dari: w.dari,
            category: w.category,
            tag: w.tag,
            example: w.example,
            context: w.context,
            contextDari: w.contextDari,
            difficulty: w.difficulty,
            isFavorite: w.isFavorite,
            isDifficult: w.isDifficult,
          ))
      .toList();

  List<VocabWord> get _filtered {
    return _words.where((w) {
      if (_tab == 'favorites' && !w.isFavorite) return false;
      if (_tab == 'difficult' && !w.isDifficult) return false;
      if (_selectedCategory != null && w.category != _selectedCategory)
        return false;
      if (_search.isNotEmpty) {
        final s = _search.toLowerCase();
        if (!w.german.toLowerCase().contains(s) &&
            !w.english.toLowerCase().contains(s)) return false;
      }
      return true;
    }).toList();
  }

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;
    if (_flashcardMode) return _buildFlashcard(isDark);
    if (_selectedWord != null) return _buildWordDetail(isDark);
    return _buildMain(isDark);
  }

