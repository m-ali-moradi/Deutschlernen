class VocabWord {
  VocabWord({
    required this.id,
    required this.german,
    required this.phonetic,
    required this.english,
    required this.dari,
    required this.category,
    required this.tag,
    required this.example,
    required this.context,
    required this.contextDari,
    required this.difficulty,
    this.isFavorite = false,
    this.isDifficult = false,
  });

  final String id;
  final String german;
  final String phonetic;
  final String english;
  final String dari;
  final String category;
  final String tag;
  final String example;
  final String context;
  final String contextDari;
  String difficulty; // easy | medium | hard
  bool isFavorite;
  bool isDifficult;

  VocabWord copyWith(
          {bool? isFavorite, bool? isDifficult, String? difficulty}) =>
      VocabWord(
        id: id,
        german: german,
        phonetic: phonetic,
        english: english,
        dari: dari,
        category: category,
        tag: tag,
        example: example,
        context: context,
        contextDari: contextDari,
        difficulty: difficulty ?? this.difficulty,
        isFavorite: isFavorite ?? this.isFavorite,
        isDifficult: isDifficult ?? this.isDifficult,
      );
}

class VocabCategory {
  const VocabCategory({
    required this.name,
    required this.icon,
    required this.gradient,
    required this.description,
  });
  final String name, icon, description;
  final List<({int r, int g, int b})> gradient; // simple RGB tuples
}

